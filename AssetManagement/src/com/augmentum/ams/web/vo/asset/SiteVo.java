package com.augmentum.ams.web.vo.asset;

public class SiteVo {
	
	private String siteType;
	
	private String siteNameCn;
	
	private String siteNameEn;
	
	private String siteNameAbbr;

	public String getSiteType() {
		return siteType;
	}

	public void setSiteType(String siteType) {
		this.siteType = siteType;
	}

	public String getSiteNameCn() {
		return siteNameCn;
	}

	public void setSiteNameCn(String siteNameCn) {
		this.siteNameCn = siteNameCn;
	}

	public String getSiteNameEn() {
		return siteNameEn;
	}

	public void setSiteNameEn(String siteNameEn) {
		this.siteNameEn = siteNameEn;
	}

	public String getSiteNameAbbr() {
		return siteNameAbbr;
	}

	public void setSiteNameAbbr(String siteNameAbbr) {
		this.siteNameAbbr = siteNameAbbr;
	}
	
}
